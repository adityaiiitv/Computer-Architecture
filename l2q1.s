 AREA lab2,CODE,READONLY
 ENTRY			; ENTRY IN THE CODE
 MOV R1,0x5000	; LOAD ADDRESS 0x5000
 LDR R2,n		; LOAD THE VALUE 10 IN R2
 MOV R3,#0		; STORE THE RESULT
LOOP			; START LOOP
 CMP R2,#0		; COMPARE WITH 0
 BEQ ENDING		; IF YES THEN SEND TO ENDING
 ADD R3,R3,R2	; ADD R3+R2
 SUB R2,R2,#1	; R2-1
 B LOOP			; BRANCH BACK TO LOOP
ENDING			; ENDING
 STR R3,[R1] 	; STORE R3 AT ADDRESS IN R1
n DCD 10	    ; USING DCD
 END			; END OF CODE